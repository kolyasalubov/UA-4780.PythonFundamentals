def largest_of_two(num1: int, num2:int) -> int:
    """
    Determines and returns the larger of two given integer numbers.

    :param num1: The first integer number to compare.
    :param num2: The second integer number to compare.
    :return: The larger of the two integer numbers.
    """
    return max(num1, num2)
